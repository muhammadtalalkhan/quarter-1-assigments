"use strict";
//Store a person’s name in a variable, and then print that person’s name in lowercase, uppercase, and titlecase.
// step number is store a person name in lowercase.
let personName = "Muhammad Talal";
let lowercaseName = personName.toLowerCase();
console.log(lowercaseName);
// step number 2 is to store a person name in uppercase.
let personName1 = "Muhammad Talal";
let uppercaseName = personName1.toUpperCase();
console.log(uppercaseName);
// step number 3 is to store a person name in titlecase
let personName2 = "muhammad talal khan";
let titlecaseName = personName2;
console.log(titlecaseName);
